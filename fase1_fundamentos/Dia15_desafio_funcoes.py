def exibir_resumo(nome, idade, hobby):
    print(f"Olá, {nome}!")
    print(f"Você tem {idade} anos e gosta de {hobby}.")

nome = input("Digite seu nome: ")
idade = input("Digite sua idade: ")
hobby = input("Digite seu hobby favorito: ")

exibir_resumo(nome, idade, hobby)
